use thiserror::Error;

/// Общее перечисление ошибок библиотеки.
#[derive(Debug, Error)]
pub enum SafeFileError {
    /// Ошибка чтения/записи или других операций ввода‑вывода.
    #[error("ввод/вывод: {0}")]
    Io(#[from] std::io::Error),

    /// Ошибка при шифровании или дешифровании.
    #[error("шифрование: {0}")]
    Crypto(String),

    /// Ошибка при подписании файла или проверке подписи.
    #[error("подпись: {0}")]
    Signature(String),

    /// Неверные входные данные (путь, размер, формат).
    #[error("валидация входных данных: {0}")]
    Validation(String),

    /// Проблемы при инициализации или использовании `Config`.
    #[error("конфигурация: {0}")]
    Config(String),
}
